#'   \item{length_k}{Length of the kernel}
#'   \item{width_k}{Width of the kernel}
#'   \item{asymmetry}{Asymmetry coefficient}
#'   \item{length_g}{Length of the kernel groove}
#'   \item{type}{Class of the seed (\code{class1} or \code{class2})}
#' }
#'
#' @source M. Charytanowicz, J. Niewczas, P. Kulczycki, P. Kowalski, and S. Lukasik. (2012) seeds.
